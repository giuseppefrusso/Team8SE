public class User {

    private String UserName;
    private String Password;
    private String Name;
    private String LastName;

    public User(){

    }

    public String getUserName() {
        return UserName;
    }

    public String getPassword() {
        return Password;
    }

    public String getName() {
        return Name;
    }

    public String getLastName() {
        return LastName;
    }
    
    
}//end User